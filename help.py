import tkinter as tk
from tkinter import ttk, messagebox

# دالة لعرض التعليمات
def show_help():
    help_text = """
    كيفية الحصول على المعلومات المطلوبة:

    1. **الواجهة اللاسلكية (Wireless Interface)**:
       - افتح Terminal واكتب الأمر التالي:
         ```
         iwconfig
         ```
       - ابحث عن الواجهة اللاسلكية (مثل wlan0 أو wlp2s0).
       - إذا لم تظهر أي واجهة لاسلكية، تأكد من أن كرت الشبكة اللاسلكية مثبت بشكل صحيح.
       - يمكنك استخدام الأمر التالي لتثبيت التعريفات:
         ```
         sudo apt install firmware-linux firmware-linux-nonfree
         ```
       - إذا ظهرت رسالة "No such device"، حاول إعادة تشغيل الجهاز أو توصيل كرت الشبكة بشكل صحيح.

    2. **BSSID (عنوان MAC للشبكة)**:
       - افتح Terminal واكتب الأمر التالي:
         ```
         sudo airodump-ng wlan0
         ```
       - ابحث عن BSSID للشبكة المستهدفة.
       - إذا لم تظهر الشبكة، تأكد من أنك في نطاق الشبكة وأن الواجهة في وضع المراقبة (Monitor Mode).
       - لتفعيل وضع المراقبة، استخدم الأمر التالي:
         ```
         sudo airmon-ng start wlan0
         ```

    3. **القناة (Channel)**:
       - في نفس النافذة التي تظهر بعد تشغيل `airodump-ng`، ستجد القناة بجانب BSSID.
       - إذا لم تظهر القناة، تأكد من أن الشبكة نشطة وأنك في نطاقها.

    4. **كلمة المرور (Password)**:
       - إذا كنت تريد تحليل كلمة مرور، أدخلها يدويًا في الحقل المخصص.
       - إذا كنت تريد كسر كلمة مرور شبكة، تأكد من أن لديك ملف Handshake أو PMKID.

    5. **مسار قائمة الكلمات (Wordlist Path)**:
       - قم بتنزيل قائمة كلمات مثل `rockyou.txt` من الإنترنت.
       - أدخل المسار الكامل للقائمة (مثل /path/to/rockyou.txt).
       - إذا لم يكن لديك قائمة كلمات، يمكنك تنزيلها باستخدام الأمر التالي:
         ```
         wget https://github.com/brannondorsey/naive-hashcat/releases/download/data/rockyou.txt
         ```
       - إذا ظهرت رسالة "File not found"، تأكد من أن المسار صحيح وأن الملف موجود.

    **مشاكل شائعة وحلولها**:

    1. **الواجهة اللاسلكية غير موجودة**:
       - تأكد من أن كرت الشبكة اللاسلكية مثبت بشكل صحيح.
       - جرب إعادة تشغيل الجهاز أو توصيل الكرت بشكل صحيح.

    2. **لا تظهر الشبكات في airodump-ng**:
       - تأكد من أن الواجهة في وضع المراقبة (Monitor Mode).
       - استخدم الأمر التالي لتفعيل وضع المراقبة:
         ```
         sudo airmon-ng start wlan0
         ```

    3. **لا يمكن كسر كلمة المرور**:
       - تأكد من أن لديك ملف Handshake أو PMKID.
       - تأكد من أن قائمة الكلمات تحتوي على كلمة المرور الصحيحة.
    """
    messagebox.showinfo("مساعدة", help_text)

# إنشاء واجهة المستخدم
root = tk.Tk()
root.title("مساعدة WiPhantom")

# زر لعرض التعليمات
help_button = ttk.Button(root, text="عرض التعليمات", command=show_help)
help_button.pack(padx=20, pady=20)

# تشغيل الواجهة
root.mainloop()
